package com.example.lms.entity.enums;

/**
 * Статус пользователя в системе.
 * Определяет, может ли пользователь участвовать в учебном процессе.
 */
public enum UserStatus {

    /**
     * Пользователь активен и может использовать систему.
     */
    ACTIVE,

    /**
     * Пользователь временно отключён от работы с системой.
     */
    INACTIVE,

    /**
     * Пользователь заблокирован и не имеет доступа к системе.
     */
    BLOCKED
}
